#include <iostream>
using namespace std;

int main()
{
    int m,n,sum;
    cin>>m>>n;
    int arr[m][n];
    for(int i=0;i<m;i++){
        for(int j=0;j<n;j++){
            cin>>arr[i][j];
            cout<<arr[i][j]<<"  ";
        }
        cout<<endl;
    }
    
    for(int i=0;i<m;i++){
        for(int j=0;j<n;j++){
            sum+=arr[i][j];
        }
        cout<<sum<<endl;
        sum=0;
    }
    return 0;
}
